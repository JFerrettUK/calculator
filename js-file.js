function calcAdd(input1, input2) {
    addResult = input1 + input2;
    return addResult;
}

function calcMin(input1, input2) {
    minResult = input1 - input2;
    return minResult;
}

function calcTimes(input1, input2) {
    timesResult = input1 * input2;
    return timesResult;
}

function calcDivide(input1, input2) {
    divideResult = input1 / input2;
    return divideResult;
}